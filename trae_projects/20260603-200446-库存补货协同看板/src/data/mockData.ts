export const generateStockTrend = (days: number = 30): StockTrend[] => {
  const trend: StockTrend[] = [];
  const today = new Date();
  
  for (let i = days - 1; i >= 0; i--) {
    const date = new Date(today);
    date.setDate(date.getDate() - i);
    
    const baseStock = 5000 + Math.sin(i / 5) * 500;
    const variation = (Math.random() - 0.5) * 300;
    
    trend.push({
      date: date.toISOString().split('T')[0],
      totalStock: Math.round(baseStock + variation),
      outOfStockCount: Math.round(15 + Math.random() * 20),
      restockCount: Math.round(5 + Math.random() * 10),
    });
  }
  
  return trend;
};

